package net.anotheria.anosite.content.bean;

public class ImageResourceBean {
	
	private String id;
	
	private String name;
	
	private String title;
	
	private String link;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String imageLink) {
		this.link = imageLink;
	}

}
